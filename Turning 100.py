# this program takes an input of a name and the year the use was born in, then displays a personalized message detailing
# what year the use will turn 100 in.

print("Hello! Please enter your name!")
name = input()

print(name + ", please what year you were born in!")
year = int(input())

print(name + ", you will turn 100 in the year " + str(year + 100) + ".")
